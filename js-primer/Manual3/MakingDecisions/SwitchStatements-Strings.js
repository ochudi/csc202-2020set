var ageRange = "below 13"; 
switch (ageRange){ 
    case "below 1": 
        console.log("Infant"); 
        break; 
    case "below 13": 
        console.log("Pre-teen"); 
        break; 
    case "below 20": 
        console.log("Teenager"); 
        break; 
    default: 
        console.log("Adult") 
} // This switch statement should print out Pre-teen.